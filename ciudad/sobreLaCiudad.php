<h2>Granada</h2>
<p class="parrafo">La ciudad de Granada está situada a los pies de Sierra Nevada,
en el Sistema Bético, el más alto de toda la península Ibérica. A parte de las vistas que ofrece desde la ciudad, 
Sierra Nevada cuenta con una de las estaciones de esquí más importantes de España, 
la más meridional de Europa y la única de Andalucía.
