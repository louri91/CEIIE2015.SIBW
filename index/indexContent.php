<p class="parrafo">Primer congreso de estudiantes de ingeniería informática en España.
La organización convoca una gran variedad de conferencias y actividades a gusto de todos
los asistentes. El propósito de este congreso es el de promover el intercambio de 
conocimiento entre los miembros de la comunidad de estudiantes de ingeniería informática.<br></p>
<h4>Organizado por la Escuela Técnica Superior de Ingenierías Informática y de Telecomunicación.</h4>
